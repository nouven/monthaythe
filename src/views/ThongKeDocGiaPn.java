	private void initTbl(DefaultTableModel model , JTable tbl, String[] headerTbl, ArrayList<DocGia> list){
		model.setRowCount(0);
		model.setColumnIdentifiers(headerTbl);
		list.forEach((elmt) -> {
			String gioiTinh = "Nam";
			if(elmt.getGoiTinh() == 0){
				gioiTinh = "nu";
			}
			model.addRow(new Object[]{
				elmt.getMaDocGia(),
				elmt.getHoTen(),
				elmt.getNgaySinh(),
				gioiTinh,
				elmt.getEmail(),
				elmt.getDiaChi(),
			});
		});
		tbl.setModel(model);
	}
